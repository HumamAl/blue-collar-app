// ─── Lookup Helpers ───────────────────────────────────────────────────────────

export const getProById = (id: string): Pro | undefined =>
  pros.find((p) => p.id === id);

export const getJobById = (id: string): Job | undefined =>
  jobs.find((j) => j.id === id);

export const getJobsByPro = (proId: string): Job[] =>
  jobs.filter((j) => j.proId === proId);

export const getBookingsByPro = (proId: string): Booking[] =>
  bookings.filter((b) => b.proId === proId);

export const getReviewsByPro = (proId: string): Review[] =>
  reviews.filter((r) => r.proId === proId);

export const getActiveJobs = (): Job[] =>
  jobs.filter((j) =>
    (["Requested", "Quoted", "Confirmed", "In Progress"] as const).includes(
      j.status as "Requested" | "Quoted" | "Confirmed" | "In Progress"
    )
  );

export const getCompletedJobs = (): Job[] =>
  jobs.filter((j) => j.status === "Completed");

export const getJobsByCategory = (category: string): Job[] =>
  jobs.filter((j) => j.category === category);

export const getActivePros = (): Pro[] =>
  pros.filter((p) => p.status === "Active");
